#Definicion de una lista
mi_lista = ['a', 'b', 'c', 'd', 'e']

#acceso usando indices positivos
primer_elemento = mi_lista [0] #a
segundo_elemento = mi_lista [1] #b

#acceso usando indices negativos
ultimo_elemento= mi_lista[-1] # 'e'
penultimo_elemento = mi_lista[-2] #'d'


print('primer elemento:', primer_elemento)
print('segundo elemento:', segundo_elemento)
print ('ultimo elemento:', ultimo_elemento)
print("penultimo elemento:", penultimo_elemento)


