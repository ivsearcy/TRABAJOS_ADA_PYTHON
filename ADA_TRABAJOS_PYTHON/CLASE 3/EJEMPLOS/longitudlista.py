#Lista Vacia
#La longitud de una lista vacia es 0
lista_vacia= []
print('longitud de lista_vacia:', len(lista_vacia)) #Devuelve 0

#Lista con enteros
#La longitud es igual al numero de elementos en la lista
lista_enteros=[1, 2, 3, 4, 5]
print('longitud de lista enteros:', len(lista_enteros))

#Lista con cadenas
# La longitud es igual al numero de elementos en la lista.
lista_cadenas = ["manzana", "banana", "cereza"]
print('Longitud de lista_cadenas:', len(lista_cadenas)) #devuelve 3

#Lista Mixta
#La longitud cuenta todos los elementos, sin importar el tipo (numeros, cadenas, listas, etc.)
lista_mixta= [1, "texto", 3.14, [1, 2]]
print('lontigud de lista_mixta:', len(lista_mixta))

#Lista con elementos repetidos
#La longitud es el resultado de multiplicar el numero de elementos repetidos
lista_repetidos = [0] * 10
print('Longitud de lista_repetidos:', len(lista_repetidos))

