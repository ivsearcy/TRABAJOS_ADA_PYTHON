x=10
print(f"x = {x}")

#suma y asignacion
x += 5
print(f"x += 5: {x}")

#Resta y asignacion
x -= 3
print(f"x -= 3: {x}")

#Multiplicacion y asignacion
x *= 4 #Equivale a x=x*4
print(f"x *= 4: {x}")

#division y asignacion
x /=2
print(f"x =/ 2: {x}")

#division entera y asignacion
x //= 3
print(f"x //= 3: {x}")

#modulo y asignacion
x &= 7
